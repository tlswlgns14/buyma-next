export function getKoreanCategoryLabel(label: string) {
  if (label === "선택 없음" || label === "지정 없음") return label;
  if (NO_SELECTION_LABELS.has(label)) return "선택 없음";
  return label
    .split(" > ")
    .map((segment) => segment.split("/").map(translateCategoryPart).join("/"))
    .join(" > ");
}

export function sortSeasonOptionsDescending<T extends BuymaOption>(options: readonly T[]) {
  const fixedOptions = options.filter((option) => isNoSelectionSeason(option));
  const seasonOptions = options.filter((option) => !isNoSelectionSeason(option));

  return [
    ...fixedOptions,
    ...seasonOptions.sort((left, right) => getSeasonSortValue(right.label, right.id) - getSeasonSortValue(left.label, left.id)),
  ];
}

function translateCategoryPart(part: string) {
  return CATEGORY_LABEL_KO[part] ?? part;
}

function isNoSelectionSeason(option: BuymaOption) {
  return option.id === "" || option.id === "0" || NO_SELECTION_LABELS.has(option.label);
}

function getSeasonSortValue(label: string, id: string) {
  const normalized = label.replace(/\s+/g, "").toUpperCase();
  const years = normalized.match(/(?:19|20)\d{2}/g) ?? [];
  const year = Number(years[years.length - 1] ?? id);
  const typeRank = normalized.includes("AW")
    ? 3
    : normalized.includes("SS")
      ? 2
      : normalized.includes("CRUISE") || normalized.includes("크루즈")
        ? 1
        : 0;

  return Number.isFinite(year) ? year * 10 + typeRank : 0;
}
